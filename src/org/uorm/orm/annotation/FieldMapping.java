package org.uorm.orm.annotation;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * 字段Mapping
 * 
 * @author <a href="mailto:xunchangguo@gmail.com">郭训常</a>
 * @version 1.0.0
 * ＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝<br/>
 * 修订日期                 修订人            描述<br/>
 * 2012-1-18       郭训常            创建<br/>
 */
@Retention(RetentionPolicy.RUNTIME)
@Target(ElementType.FIELD)
public @interface FieldMapping {
	/** column's name */
    String columnName();
    /**column's type. in SQL type*/
    int columnType() default 12;
    /** is primary key */
    boolean primary() default false;
   /** for save, true = column is from table, false = column is from a view/query only. */
    boolean includeInWrites() default true;
    /** for update, ture = column will be update when no filter null, false = column will be not update ,even filter null */
    boolean includeInUpdate() default true;
}
